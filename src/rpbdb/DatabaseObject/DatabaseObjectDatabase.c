/*******************************************************************************************************************************************************************************************
																		Ruby Definitions
*******************************************************************************************************************************************************************************************/

extern	VALUE	rb_RPbdb_Database;
extern	VALUE	rb_RPbdb_DatabaseController;
extern	VALUE	rb_RPbdb_DatabaseObjectCursor;
extern	VALUE	rb_RPbdb_DatabaseObjectDatabase;

void Init_rb_RPbdb_DatabaseObjectDatabase()	{
	
	rb_RPbdb_DatabaseObjectDatabase		=	rb_define_class_under(	rb_RPbdb_DatabaseController, 
																															"ObjectDatabase",
																															rb_RPbdb_Database );

	rb_define_method(		rb_RPbdb_DatabaseObjectDatabase, 	"cursor",										rb_RPbdb_DatabaseObjectDatabase_cursor,						0 	);
		
}

/*******************************************************************************************************************************************************************************************
********************************************************************************************************************************************************************************************
													Module Definition (Ruby Init)
********************************************************************************************************************************************************************************************
*******************************************************************************************************************************************************************************************/

/***********
*  cursor  *
***********/

VALUE rb_RPbdb_DatabaseObjectDatabase_cursor( VALUE	rb_database )	{

	RPbdb_Database*	c_database	=	NULL;
	C_RPBDB_DATABASE_OBJECT_DATABASE( rb_database, c_database );
	
	RPbdb_DatabaseCursor*	c_cursor	=	RPbdb_Database_cursor( c_database );
	
	VALUE	rb_cursor	=	RUBY_RPBDB_DATABASE_OBJECT_CURSOR( c_cursor );
	
	return rb_cursor;
}	

